void main() {
  final int a = 1, b = 2;

  void add() {
    int c = a + b;
    print(c);
  }

  add();

  void subtract() {
    int d = a - b;
    print(d);
  }

  subtract();

  void divide() {
    double e = a / b;
    print(e);
  }

  divide();

  void multiply() {
    int f = a * b;
    print(f);
  }

  multiply();
}
